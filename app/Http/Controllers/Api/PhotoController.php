     * @param  \App\Models\Photo  $photo
     * @return \Illuminate\Http\Response
     */
    public function show(Photo $photo)
    {
        $n = 0;
        $pictures = [];

        foreach ($photo->media as $pic) {
            $pictures[$n]['url'] = $pic->getFullUrl();

            $n++;
        }

        $photo->pictures = $pictures;
        unset($photo->media);

        return $photo;
    }

